import { ApiProperty } from '@nestjs/swagger';
import { Type } from 'class-transformer';
import {
  IsDefined,
  IsEnum,
  IsNotEmpty,
  IsNotEmptyObject,
  IsObject,
  IsString,
  IsUUID,
  ValidateNested,
} from 'class-validator';
import { ListEnum } from '../enum/ListEnum';

class AttributesObject {
  @IsString()
  @IsNotEmpty()
  @ApiProperty({
    description: 'A helpful name to label the list',
    example: 'Newsletter',
    default: 'Newsletter',
  })
  name: string;
}

class DataObject {
  @IsDefined()
  @IsEnum(ListEnum, { each: true })
  @ApiProperty({
    enum: ListEnum,
    default: ListEnum.List,
  })
  type: ListEnum;

  @IsNotEmpty()
  @IsUUID()
  @ApiProperty({
    description:
      'Primary key that uniquely identifies this list. Generated by Bespoke.',
    default: 'UUID',
    example: 'UUID',
  })
  id: string;

  @IsObject()
  @ValidateNested()
  @IsNotEmptyObject()
  @IsDefined()
  @Type(() => AttributesObject)
  @ApiProperty()
  attributes: AttributesObject;
}

export class UpdateListDto {
  @IsObject()
  @ValidateNested()
  @IsNotEmptyObject()
  @IsDefined()
  @Type(() => DataObject)
  @ApiProperty()
  data: DataObject;
}
